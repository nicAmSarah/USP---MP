operacao = input()
int1 = int(input())
int2 = int(input())

if (operacao == "+"):
    print(int1 + int2)
elif (operacao == "-"):
    print(int1 - int2)
elif (operacao == "/"):
    print(int1 / int2)
elif (operacao == "*"):
    print(int1 * int2)
elif (operacao == "//"):
    print(int1 // int2)
elif (operacao == "%"):
    print(int1 % int2)
elif (operacao == "**"):
    print(int1 ** int2)
    